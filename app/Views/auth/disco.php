    <p class="card-text">Born to Die is the second studio album and major label debut by American singer-songwriter Lana Del Rey. It was released on January 27, 2012, through Interscope Records and Polydor Records. The album was reissued on November 9, 2012, as an expanded version subtitled The Paradise Edition.

Prior to the release of the album, Del Rey had attracted attention with her 2011 singles "Video Games" and "Born to Die", which contrasted with contemporary electronic/dance music with their cinematic sound accompanied by dramatic string instruments. A predominantly baroque pop and trip hop album, Born to Die musically features the same cinematic composition. The lyrics are about love, sex, and drugs, and features prominent references to 1950s and 1960s Americana. The album was the world's fifth best-selling album of 2012, and had sold over 7 million copies by 2014. It spent more than 300 weeks on the US Billboard 200, where it peaked at number two, and topped charts in Australia and various European countries including France, Germany, and the UK.

